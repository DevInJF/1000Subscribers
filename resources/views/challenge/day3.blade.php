			<h2>Location, Location, Location</h2>
			<p>This is the #1 rule of real estate. Location is everything.</p>
			<p>Buy why?</p>
			<p>The reason why is because the success of your store is dependent on foot traffic. If you are in a location that nobody frequents then you will have a tough time bringing people in.</p>
			<p>However, if you&rsquo;re a bar that opens across the street from the new football stadium then things are going to be looking up for you.</p>
