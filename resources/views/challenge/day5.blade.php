			<h2>Improving Your Conversion Rate</h2>
			<p>So you want to improve your conversion rate. How do you do that?</p>
			<p>It depends on how you have your site setup. We will take a look at a couple of different things here.</p>
			<h3>After Blog Post Form</h3>
			<p>One of the best places to put an email form is directly after a blog post. The reason behind this is that your reader is primed and ready to make a move.</p>
			<p>If you find that your offer isn&rsquo;t converting as well (like my 1.4% conversion rate) as you would want then here are some reasons why it might be underperforming:</p>
			<ul>
				<li>Attention-grabbing headline isn&rsquo;t grabbing anyone&rsquo;s attention.</li>
				<li>The offer isn&rsquo;t good enough. Plain and simple.</li>
				<li>The content upgrade section is easily bypassed. You can&rsquo;t assume a reader sees everything. Is the content upgrade section too easy to miss? Make it a different background color than your blog post. Add an image of the content upgrade.</li>
			</ul>
			<h3>Add Landing Pages</h3>
			<p>As you saw with my own account, landing pages clearly outperform random forms around your site. The reason for this is because landing pages clearly define what the person is getting and it&rsquo;s the only thing to do on the page.</p>
			<p>A landing page makes a content upgrade feel special. Almost like the reader is getting a full-fledged product for free!</p>
			<p>However, every content upgrade doesn&rsquo;t deserve a landing page. If the content upgrade was made specifically for a post, like a checklist, then there is no reason to spend time making a full landing page for it.</p>
			<p>If it is a content upgrade that you reference across a number of different posts then it might be something to consider for a landing page.</p>
